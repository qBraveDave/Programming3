var side = 25;
var grassArr = [];
var xotakerner = [];
var gishatichner = [];
var mah = [];

function setup() {
    frameRate(10);
   noStroke();
    createCanvas(matrix[2].length * side, matrix.length * side);
    background('#acacac');
    for (var y = 0; y < matrix.length; ++y) {
        for (var x = 0; x < matrix[y].length; ++x) {
            if (matrix[y][x] == 1) {
                var gr = new Grass(x, y, 1);
                grassArr.push(gr);
            }
            else if (matrix[y][x] == 2) {
                xotakerner.push(new Xotaker(x, y, 2));
            }
            else if (matrix[y][x] == 3) {
                gishatichner.push(new Gishatich(x, y, 3))
            }
            else if (matrix[y][x] == 6) {
                mah.push(new Mah(x, y, 6))
            }
        }
    }
}
function draw() {

    for (var y = 0; y < matrix.length; y++) {
        for (var x = 0; x < matrix[y].length; x++) {

            if (matrix[y][x] == 1) {
                fill("green");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 0) {
                fill("#acacac");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 2) {
                fill("yellow");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 3) {
                fill("blue");
                rect(x * side, y * side, side, side);
            }
            else if (matrix[y][x] == 6) {
                fill("black");
                rect(x * side, y * side, side, side)
            }
            else if (matrix[y][x] == 9) {
                fill("#7b7b8c");
                rect(x * side, y * side, side, side);
            }
        }
    }
    for (var i in grassArr) {
        grassArr[i].bazmanal();
    }
    for (var i in xotakerner) {
        xotakerner[i].utel();
        xotakerner[i].mahanal();
    }
    for (var i in gishatichner) {
        gishatichner[i].utel();
        gishatichner[i].mahanal();
    }
    for (var i in mah) {
        mah[i].utelXotaker();
        mah[i].utelGishatich();
    }
}

